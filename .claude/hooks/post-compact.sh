#!/bin/bash
# ============================================================
# post-compact.sh
# 压缩后：提醒用户读取会话状态文件恢复上下文
# 输出提醒信息到 stdout
# ============================================================

set -euo pipefail

STATE_FILE="production/session-state/active.md"

echo "========================================"
echo "  会话已压缩"
echo "========================================"
echo ""
echo "提醒：会话已压缩。请读取 $STATE_FILE 恢复上下文并从上次中断处继续。"
echo ""

# 如果状态文件存在，显示简要提示
if [ -f "$STATE_FILE" ]; then
  echo "已找到会话状态文件，建议执行以下操作："
  echo "  1. 读取 $STATE_FILE 查看上次进度"
  echo "  2. 根据压缩前快照恢复上下文"
  echo "  3. 从上次中断处继续工作"
  echo ""
  echo "会话状态文件最后更新内容预览："
  echo "----------------------------------------"
  tail -n 10 "$STATE_FILE" 2>/dev/null | sed 's/^/  /'
  echo "----------------------------------------"
else
  echo "未找到会话状态文件：$STATE_FILE"
  echo "可能是首次会话或状态文件已归档。"
  echo "请直接开始新任务。"
fi

echo ""
echo "========================================"

exit 0